    // Construct the WhatsApp link dengan parameter text=
    var whatsappLink =
      "https://api.whatsapp.com/send?phone=6285779678006&text=" +
      whatsappMessage;

    // Buka WhatsApp di tab baru
    window.open(whatsappLink, "_blank");

    // Kosongkan form setelah mengirim
